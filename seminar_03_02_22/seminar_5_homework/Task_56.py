#Дан список чисел. Выделить среди них числа, удовлетворяющие условию:следующее больше предыдущего. 
# Пример: [1, 5, 2, 3, 4, 6, 1, 7] => [1, 2, 3] или [1, 7] или [1, 6, 7] и т.д.

list1 = [1, 5, 2, 3, 4, 6, 1, 7]
list2_1 = []
temp = list1[0]
list2_1.append(temp)
for i in list1:
    if i > list2_1[-1]:
        list2_1.append(i)
    
    
print(list1)
print(list2_1)
